package LINKEDLIST;

public class Ejemplo2Cliente {

    private String nombre;
    private String servivio;

    public Ejemplo2Cliente(String nombre, String servivio) {
        this.nombre = nombre;
        this.servivio = servivio;
    }

    public String getNombre() {
        return nombre;
    }

    public void setNombre(String nombre) {
        this.nombre = nombre;
    }

    public String getServivio() {
        return servivio;
    }

    public void setServivio(String servivio) {
        this.servivio = servivio;
    }    

}
