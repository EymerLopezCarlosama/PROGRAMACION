package STIVEN;

public class Electrodomesticos {
    int numeroSerial;
    String marcaElectrodomestico;
    double precioElectrodomestico;

    public int getNumeroSerial() {
        return numeroSerial;
    }

    public void setNumeroSerial(int numeroSerial) {
        this.numeroSerial = numeroSerial;
    }

    public String getMarcaElectrodomestico() {
        return marcaElectrodomestico;
    }

    public void setMarcaElectrodomestico(String marcaElectrodomestico) {
        this.marcaElectrodomestico = marcaElectrodomestico;
    }

    public double getPrecioElectrodomestico() {
        return precioElectrodomestico;
    }

    public void setPrecioElectrodomestico(double precioElectrodomestico) {
        this.precioElectrodomestico = precioElectrodomestico;
    }
}
